function uuid() {
  return crypto.randomUUID();
}

function w(
  text: string,
  translation: string[] = [],
  options: Record<string, any> = {},
): any {
  return { text, translation, isBlank: false, audioUrl: null, ...options };
}

function wBold(
  text: string,
  translation: string[] = [],
  highlight = false,
): any {
  return {
    text,
    translation,
    isBlank: false,
    audioUrl: null,
    bold: true,
    highlight,
  };
}

function wHighlight(text: string, translation: string[] = []): any {
  return {
    text,
    translation,
    isBlank: false,
    audioUrl: null,
    bold: true,
    highlight: true,
  };
}

function explain(
  seq: number,
  words: any[],
  alignment: "left" | "center" | "right" = "left",
  size = 16,
): any {
  return {
    id: uuid(),
    type: "explain",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    text: words,
    alignment,
    size,
  };
}

function reading(seq: number, words: any[]): any {
  return {
    id: uuid(),
    type: "reading",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    text: words,
    audioUrl: "",
    isHide: false,
    isReadable: true,
  };
}

function audio(seq: number): any {
  return {
    id: uuid(),
    type: "audio",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    audio: "",
  };
}

function chat(
  seq: number,
  name: string,
  position: "left" | "right",
  words: any[],
): any {
  return {
    id: uuid(),
    type: "chat",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    sender: { name, imageUrl: "" },
    position,
    text: words,
    audioUrl: "",
    isDisplay: true,
    isReadable: true,
  };
}

function image(seq: number): any {
  return {
    id: uuid(),
    type: "image",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    url: "",
    height: 200,
  };
}

function column(seq: number, actions: any[]): any {
  return {
    id: uuid(),
    type: "column",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    ratio: "1:1",
    actions,
  };
}

function choice(seq: number, items: { text: any; isCorrect: boolean }[]): any {
  return {
    id: uuid(),
    type: "choice",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    items,
  };
}

function matchCard(seq: number, pairs: { left: string; right: string }[]): any {
  return {
    id: uuid(),
    type: "match_card",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    items: pairs.map((p) => ({
      left: { text: p.left },
      right: { text: p.right },
    })),
  };
}

function reorder(
  seq: number,
  words: { text: string; translation?: string[]; seqNum: number }[],
): any {
  return {
    id: uuid(),
    type: "reorder",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    items: words.map((wd) => ({
      text: {
        text: wd.text,
        translation: wd.translation || [],
        isBlank: false,
      },
      sequence: wd.seqNum,
    })),
  };
}

function fillByTyping(
  seq: number,
  parts: { text: string; isBlank: boolean }[],
): any {
  return {
    id: uuid(),
    type: "fill_sentence_by_typing",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    sentence: parts.map((p) => ({
      text: p.text,
      isBlank: p.isBlank,
      inSentence: true,
    })),
  };
}

function fillWithChoice(
  seq: number,
  sentenceParts: {
    text: string;
    translation?: string[];
    isBlank: boolean;
    inSentence: boolean;
  }[],
): any {
  return {
    id: uuid(),
    type: "fill_sentence_with_choice",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    sentence: sentenceParts.map((p) => ({
      text: {
        text: p.text,
        translation: p.translation || [],
        isBlank: false,
      },
      isBlank: p.isBlank,
      inSentence: p.inSentence,
    })),
  };
}

function writeSentence(seq: number, words: string[]): any {
  return {
    id: uuid(),
    type: "write_sentence",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    sentence: words,
    expectSentence: words,
  };
}

function writeSentenceInChat(
  seq: number,
  words: string[],
  position: "left" | "right" = "right",
): any {
  return {
    id: uuid(),
    type: "write_sentence_in_chat",
    sequence: seq,
    marginTop: 10,
    marginBottom: 0,
    sentence: words,
    expectSentence: words,
    position,
  };
}

function screen(seq: number, actions: any[]): any {
  return { id: uuid(), sequence: seq, actions };
}

function choiceItem(
  text: string,
  translation: string[],
  isCorrect: boolean,
): any {
  return {
    text: { text, translation, isBlank: false },
    isCorrect,
  };
}

